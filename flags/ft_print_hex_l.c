#include <unistd.h>

/*
* Handle the x flag to print hex lowercase
*/
void	print_hex_l(int n)
{
	if (n >= 16)
		print_hex_l(n / 16);
	n = n % 16;
	n += n < 10 ? '0' : 'a' - 10;
	write(1, &n, 1);
}
